import { useEffect, useRef, useState } from 'react'
import classNames from 'classnames/bind'

import styles from './Map.module.scss'
import mapApi from '@/apis/mapApi'
import { BuildingQuery } from '@/models/building'

declare global {
  interface Window {
    kakao: any
  }
}

const cx = classNames.bind(styles)

function Map() {
  const [a, setA] =
    useState<Omit<BuildingQuery, 'legalDongCode' | 'cityDistrictCode'>>()

  const mapContainer = useRef(null)

  // const handleMarkerClick = (buildingInfo: any) => {
  //   return function () {
  //     console.log(buildingInfo)
  //   }
  // }

  useEffect(() => {
    const script = document.createElement('script')
    script.src = `https://dapi.kakao.com/v2/maps/sdk.js?appkey=${process.env.REACT_APP_KAKAO_APP_KEY}&autoload=false&libraries=drawing`
    script.async = true

    document.head.appendChild(script)

    script.onload = () => {
      window.kakao.maps.load(() => {
        const position = new window.kakao.maps.LatLng(
          37.496486063,
          127.028361548,
        )
        const firstOptions = {
          center: position,
          level: 3,
        }

        const map = new window.kakao.maps.Map(
          mapContainer.current,
          firstOptions,
        )

        window.kakao.maps.event.addListener(map, 'bounds_changed', function () {
          const bounds = map.getBounds()
          const swLatlng = bounds.getSouthWest()
          const neLatlng = bounds.getNorthEast()

          setA({
            lat_n: neLatlng.Ma,
            lng_n: neLatlng.La,
            lat_s: swLatlng.Ma,
            lng_s: swLatlng.La,
          })
        })
      })
    }
  }, [])

  return (
    <div ref={mapContainer} className={cx('map')}>
      <button
        className={cx('research-btn')}
        onClick={async () => {
          const res = await mapApi.getBuildings({
            ...a!,
            legalDongCode: '10100',
            cityDistrictCode: '11650',
          })

          console.log(res)
        }}
      >
        정보받아오기
      </button>
    </div>
  )
}

export default Map

